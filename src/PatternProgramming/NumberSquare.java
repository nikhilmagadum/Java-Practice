package PatternProgramming;

public class NumberSquare {

    public static void main(String[] args) {
        // Use nested for loops to print the given pattern
        for (int i = 1; i <= 4; i++) {
            for (int j = 1; j<= 4; j++) {
                System.out.print(i);
            }
            System.out.println();
        }
    }
}
